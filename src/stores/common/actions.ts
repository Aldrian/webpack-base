/**
 * Define common Actions type here
 */

// gonna be used by reducer
export type TCommonStoreActions = {
  type: string;
  payload: any;
  currentPageName: string;
};

// define each actions in enum
export enum ECommonStoreActionsTypes {
  SET_CURRENT_STEP = "SET_CURRENT_PAGE_NAME"
}

/**
 * setcurrentPageName exemple
 * @param currentPageName
 */
export const setcurrentPageName = currentPageName => ({
  type: ECommonStoreActionsTypes.SET_CURRENT_STEP,
  currentPageName
});
